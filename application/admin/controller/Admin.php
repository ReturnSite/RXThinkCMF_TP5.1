namespace app\admin\controller;

use app\admin\service\AdminService;
use app\common\controller\Backend;

/**
 * 人员管理-控制器
 * @author 牧羊人
 * @since 2020/7/11
 * Class Admin
 * @package app\admin\controller
 */
class Admin extends Backend
{
    /**
     * 初始化
     * @author 牧羊人
     * @since 2020/7/11
     */
    public function initialize()
    {
        parent::initialize();
        $this->model = new \app\admin\model\Admin();
        $this->service = new AdminService();
        $this->validate = new \app\admin\validate\Admin();
    }

    /**
     * 添加或编辑
     * @return mixed
     * @throws \think\db\exception\DataNotFoundException
     * @throws \think\db\exception\ModelNotFoundException
     * @throws \think\exception\DbException
     * @author 牧羊人
     * @since 2020/7/11
     */
    public function edit()
    {
        // 获取上级部门
        $deptMod = new \app\admin\model\Dep();
        $result = $deptMod->getChilds(0, true);
        if ($result) {
            $deptList = array();
            foreach ($result as $val) {
                $key = (int)$val['id'];
                $deptList[$key] = $val;
                $vlist = isset($val['children']) ? $val['children'] : [];
                if ($vlist) {
                    foreach ($vlist as &$v) {
                        $k = (int)$v['id'];
                        $v['name'] = "|--" . $v['name'];
                        $deptList[$k] = $v;
                        $clist = isset($v['children']) ? $v['children'] : [];
                        if ($clist) {
                            foreach ($clist as &$vt) {
                                $kt = (int)$vt['id'];
                                $vt['name'] = "|--|--" . $vt['name'];
                                $deptList[$kt] = $vt;
                            }
                        }
                    }
                }
            }
        }
        // 绑定数据
        $this->assign("deptList", $deptList);
        return parent::edit(); // TODO: Change the autogenerated stub
    }

    /**
     * 重置密码
     * @return mixed
     * @author 牧羊人
     * @date 2019/5/10
     */
    public function resetPwd()
    {
        if (IS_POST) {
            $result = $this->service->resetPwd();
            return $result;
        }
    }
}
